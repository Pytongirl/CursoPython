numeros_ordinales=[1, 2, 6, 4, 7, 3, 5, 8, 9 ]

for ordinales in numeros_ordinales:
    if ordinales==1:
        print("1 -> primero")
    elif ordinales == 2:
        print("2 -> segundo")
    elif ordinales == 3:
        print("3 -> tercero")
    elif ordinales == 4:
        print("4 -> cuarto")
    elif ordinales == 5:
        print("5 -> quinto")
    elif ordinales ==6:
        print("6 -> sexto")
    elif ordinales ==7:
        print("7 -> septimo")
    elif ordinales ==8:
        print("8 -> octavo")
    else:
        print("9 -> noveno" )
    


